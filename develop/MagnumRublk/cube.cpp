void Rublk::confuse()
{
  std::random_device rd;
  std::default_random_engine engine(rd());
  std::uniform_int_distribution<> dis(0, 3);

  int degrees[6] = {0};
  int count = 10 * (1 + dis(engine));
  for (int i = 0; i < count; ++i) {
    for (int j = 0; j < 6; ++j) {
      degrees[j] = 90 * dis(engine);
    }

    for (auto& c: m_pImpl->m_cubes) {
      Vector3 p = c->getPosition();
      if (p.x() < -1.0f) {
        c->setRotation(degrees[0], Vector3(-1.0f, 0.0f, 0.0f));
      }
    }
   for (auto& c: m_pImpl->m_cubes) {
      Vector3 p = c->getPosition();
      if (p.x() > 1.0f) {
        c->setRotation(degrees[1], Vector3(1.0f, 0.0f, 0.0f));
      }
    }
    for (auto& c: m_pImpl->m_cubes) {
      Vector3 p = c->getPosition();
      if (p.y() < -1.0f) {
        c->setRotation(degrees[2], Vector3(0.0f, -1.0f, 0.0f));
      }
     }
    for (auto& c: m_pImpl->m_cubes) {
      Vector3 p = c->getPosition();
      if (p.y() > 1.0f) {
        c->setRotation(degrees[3], Vector3(0.0f, 1.0f, 0.0f));
      }
    }
    for (auto& c: m_pImpl->m_cubes) {
      Vector3 p = c->getPosition();
      if (p.z() < -1.0f) {
        c->setRotation(degrees[4], Vector3(0.0f, 0.0f, -1.0f));
      }
    }
    for (auto& c: m_pImpl->m_cubes) {
      Vector3 p = c->getPosition();
      if (p.z() > 1.0f) {
        c->setRotation(degrees[5], Vector3(0.0f, 0.0f, 1.0f));
      }
    }
  }
}

