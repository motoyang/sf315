using namespace Magnum;

class Cube: public Object3D, public SceneGraph::Drawable3D
{
  int m_id;
  Vector3 m_position;

  int m_angle;
  Vector3 m_axis;

  GL::Texture2D &m_texture, &m_texDiffuse;
  Shaders::Phong &m_shader;
  GL::Buffer m_buffer;
  GL::Mesh m_mesh;

public:
  explicit Cube(Object3D* parent, SceneGraph::DrawableGroup3D* group,
                int id, Float* vertices, const Vector3& position,
                Shaders::Phong& shader, GL::Texture2D& texture,
                GL::Texture2D& texDiffuse);

  Cube(Cube&& c) = delete;
  Cube& operator=(Cube&& c) = delete;
  Cube(const Cube& c) = delete;
  Cube& operator=(const Cube& c) = delete;

  Vector3 getPosition() const;
  void roll(int degree, const Vector3& axis);
  void setRotation(int degree, const Vector3& axis);

private:
  void draw(const Matrix4& transformationMatrix, SceneGraph::Camera3D& camera) override;
};

Cube::Cube(Object3D *parent, SceneGraph::DrawableGroup3D *group,
           int id, Float *vertices, const Vector3 &position,
           Shaders::Phong& shader, GL::Texture2D& texture,
           GL::Texture2D& texDiffuse)
  : Object3D(parent)
  , SceneGraph::Drawable3D(*this, group)
  , m_id(id)
  , m_position(position)
  , m_angle(-1)
  , m_axis()
  , m_texture(texture)
  , m_texDiffuse(texDiffuse)
  , m_shader(shader)
{
  Corrade::Containers::ArrayView<Float> av {vertices, CubeVertexSize};
  m_buffer.setData(av, GL::BufferUsage::StaticDraw);
  m_mesh.setPrimitive(GL::MeshPrimitive::Triangles)
      .setCount(36)
      .addVertexBuffer(m_buffer, 0,
                       Shaders::Phong::Position{},
                       Shaders::Phong::Normal{},
                       Shaders::Phong::TextureCoordinates{});

  translate(m_position);
}

Vector3 Cube::getPosition() const
{
  return transformationMatrix().transformVector(m_position);
}

void Cube::roll(int degree, const Vector3& axis)
{
  m_angle = degree;
  m_axis = axis;
  g_app->_rublk->taskStart();
}

void Cube::setRotation(int degree, const Vector3 &axis)
{
  rotate(Math::Deg<Float>{static_cast<Float>(degree)}, axis);
}

void Cube::draw(const Matrix4 &transformationMatrix, SceneGraph::Camera3D &camera)
{
  using namespace Magnum::Math::Literals;

  if (m_angle == 0) {
    g_app->_rublk->taskFinished();
    m_angle = -1;
  }

  if (m_angle > 0) {
    rotate(3.0_degf, m_axis);
    m_angle -= 3;
  }
/*
  auto d1 = (transformationMatrix)[0].xyz().dot();
  auto d2 = (transformationMatrix)[1].xyz().dot();
  auto d3 = (transformationMatrix)[2].xyz().dot();
  if (!Math::TypeTraits<Float>::equals(d1, d2) || !Math::TypeTraits<Float>::equals(d2, d3)) {
    Debug() << d1 << " = " << d2 << " = " << d3;
  }
*/
  m_shader.setTransformationMatrix(transformationMatrix)
      .setNormalMatrix(transformationMatrix.rotation())
      .setProjectionMatrix(camera.projectionMatrix())
      .bindDiffuseTexture(m_texDiffuse)
      .bindSpecularTexture(m_texture);

  m_mesh.draw(m_shader);
}

