class Rublk::Impl
{
public:
  int m_taskCount;
  std::vector<std::unique_ptr<Cube>> m_cubes;
  std::queue<char> m_events;

  Shaders::Phong m_shader;
  GL::Texture2D m_texture, m_texDiffuse;

  Impl()
    : m_taskCount(0)
    , m_shader(Shaders::Phong::Flag::DiffuseTexture | Shaders::Phong::Flag::SpecularTexture)
  {
    m_shader.setLightPosition({3.0f, 5.0f, 7.0f})
//        .setLightColor(Color3{1.0f})
//        .setDiffuseColor(Color3{1.0f})
        .setAmbientColor(Color3{0.1f});

    PluginManager::Manager<Trade::AbstractImporter> manager;
    std::unique_ptr<Trade::AbstractImporter> importer = manager.loadAndInstantiate("PngImporter");
    CORRADE_INTERNAL_ASSERT(importer);

    /* Load the texture */
    const Utility::Resource rs{"magnum-rublk-data"};
    CORRADE_INTERNAL_ASSERT_OUTPUT(importer->openData(rs.getRaw("textures/rublk.png")));

    /* Set texture data and parameters */
    Containers::Optional<Trade::ImageData2D> image = importer->image2D(0);
    CORRADE_INTERNAL_ASSERT(image);
    m_texture.setWrapping(GL::SamplerWrapping::ClampToEdge)
        .setMagnificationFilter(GL::SamplerFilter::Linear)
        .setMinificationFilter(GL::SamplerFilter::Linear)
        .setStorage(1, GL::TextureFormat::RGB8, image->size())
        .setSubImage(0, {}, *image);

    CORRADE_INTERNAL_ASSERT_OUTPUT(importer->openData(rs.getRaw("textures/rublk_diffuse.png")));

    /* Set texture data and parameters */
    image = importer->image2D(0);
    CORRADE_INTERNAL_ASSERT(image);
    m_texDiffuse.setWrapping(GL::SamplerWrapping::ClampToEdge)
        .setMagnificationFilter(GL::SamplerFilter::Linear)
        .setMinificationFilter(GL::SamplerFilter::Linear)
        .setStorage(1, GL::TextureFormat::RGB8, image->size())
        .setSubImage(0, {}, *image);
  }

  void eventHandler()
  {
    // 如果event队列为空，就是没有需要处理的event
    if (m_events.size() == 0) {
      return;
    }

    // 如果仍然有任务在执行，就不做事件处理
    if (m_taskCount) {
      return;
    }

    // 提取队列中的第一个event
    char c = m_events.front();
    m_events.pop();

    // 处理event
    switch (c) {
    case 'l':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.x() < -1.0f) {
          c->roll(90, Vector3(-1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'L':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.x() < -1.0f) {
          c->roll(90, Vector3(1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'r':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.x() > 1.0f) {
          c->roll(90, Vector3(1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'R':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.x() > 1.0f) {
          c->roll(90, Vector3(-1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'f':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.z() > 1.0f) {
          c->roll(90, Vector3(0.0f, 0.0f, 1.0f));
        }
      });
      break;
    case 'F':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.z() > 1.0f) {
          c->roll(90, Vector3(0.0f, 0.0f, -1.0f));
        }
      });
      break;
    case 'b':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.z() < -1.0f) {
          c->roll(90, Vector3(0.0f, 0.0f, -1.0f));
        }
      });
      break;
    case 'B':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.z() <- 1.0f) {
          c->roll(90, Vector3(0.0f, 0.0f, 1.0f));
        }
      });
      break;
    case 'u':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.y() > 1.0f) {
          c->roll(90, Vector3(0.0f, 1.0f, 0.0f));
        }
      });
      break;
    case 'U':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.y() > 1.0f) {
          c->roll(90, Vector3(0.0f, -1.0f, 0.0f));
        }
      });
      break;
    case 'd':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.y() < -1.0f) {
          c->roll(90, Vector3(0.0f, -1.0f, 0.0f));
        }
      });
      break;
    case 'D':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](auto& c) {
        Vector3 p = c->getPosition();
        if (p.y() < -1.0f) {
          c->roll(90, Vector3(0.0f, 1.0f, 0.0f));
        }
      });
      break;
    default:
      break;
    }
  }
};

