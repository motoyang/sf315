class Rublk::Impl
{
public:
  int m_rank;
  int m_taskCount;
  unsigned int m_diffuseMap;
  unsigned int m_specularMap;
  std::vector<Cube> m_cubes;
  std::queue<char> m_events;

  void eventHandler()
  {
    // 如果event队列为空，就是没有需要处理的event
    if (m_events.size() == 0) {
      return;
    }

    // 如果仍然有任务在执行，就不做事件处理
    if (m_taskCount) {
      return;
    }

    // 提取队列中的第一个event
    char c = m_events.front();
    m_events.pop();

    // 处理event
    switch (c) {
    case 'l':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.x < -1.0) {
          c.rotate(90, glm::vec3(-1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'L':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.x < -1.0) {
          c.rotate(90, glm::vec3(1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'r':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.x > 1.0) {
          c.rotate(90, glm::vec3(1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'R':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.x > 1.0) {
          c.rotate(90, glm::vec3(-1.0f, 0.0f, 0.0f));
        }
      });
      break;
    case 'f':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.z > 1.0) {
          c.rotate(90, glm::vec3(0.0f, 0.0f, 1.0f));
        }
      });
      break;
    case 'F':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.z > 1.0) {
          c.rotate(90, glm::vec3(0.0f, 0.0f, -1.0f));
        }
      });
      break;
    case 'b':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.z < -1.0) {
          c.rotate(90, glm::vec3(0.0f, 0.0f, -1.0f));
        }
      });
      break;
    case 'B':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.z <- 1.0) {
          c.rotate(90, glm::vec3(0.0f, 0.0f, 1.0f));
        }
      });
      break;
    case 'u':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.y > 1.0) {
          c.rotate(90, glm::vec3(0.0f, 1.0f, 0.0f));
        }
      });
      break;
    case 'U':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.y > 1.0) {
          c.rotate(90, glm::vec3(0.0f, -1.0f, 0.0f));
        }
      });
      break;
    case 'd':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.y < -1.0) {
          c.rotate(90, glm::vec3(0.0f, -1.0f, 0.0f));
        }
      });
      break;
    case 'D':
      std::for_each(m_cubes.begin(), m_cubes.end(), [](Cube& c) {
        glm::vec3 p = c.getPosition();
        if (p.y < -1.0) {
          c.rotate(90, glm::vec3(0.0f, 1.0f, 0.0f));
        }
      });
      break;
    default:
      break;
    }
  }
};

