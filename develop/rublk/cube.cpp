class Cube
{
  int m_id;
  glm::vec3 m_position;
  glm::mat4 m_model;
  unsigned int m_vao, m_vbo;

  int m_angle;
  glm::vec3 m_axis;

  void deleteGlObjects();

public:
  Cube(int id, const float* vertices, const glm::vec3& position);
  virtual ~Cube();
  Cube(Cube&& c) noexcept;
  Cube& operator=(Cube&& c) noexcept; // 可以是delete的
  Cube(const Cube& c) = delete;
  Cube& operator=(const Cube& c) = delete;

  void render(const Shader& s);
  glm::vec3 getPosition() const;
  void rotate(int degree, const glm::vec3& axis);
  void changeModel(int degree, const glm::vec3& axis);
};

void Cube::deleteGlObjects()
{
  if (m_vao) {
    glDeleteVertexArrays(1, &m_vao);
    m_vao = 0;
  }
  if (m_vbo) {
    glDeleteBuffers(1, &m_vbo);
    m_vbo = 0;
  }
}

Cube::Cube(int id, const float* vertices, const glm::vec3 &position)
  : m_id(id)
  , m_position(position)
  , m_angle(-1)
{
  glGenBuffers(1, &m_vbo);
  glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
  glBufferData(GL_ARRAY_BUFFER, sizeof(v27[0]), vertices, GL_STATIC_DRAW);

  glGenVertexArrays(1, &m_vao);
  glBindVertexArray(m_vao);
  glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
  glEnableVertexAttribArray(0);
  glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
  glEnableVertexAttribArray(1);
  glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
  glEnableVertexAttribArray(2);

  glBindVertexArray(0);
  glBindBuffer(GL_ARRAY_BUFFER, 0);
}

Cube::~Cube()
{
  deleteGlObjects();
}

Cube::Cube(Cube &&c) noexcept
  : m_id(c.m_id)
  , m_position(std::move(c.m_position))
  , m_model(std::move(c.m_model))
  , m_vao(c.m_vao)
  , m_vbo(c.m_vbo)
  , m_angle(c.m_angle)
  , m_axis(std::move(c.m_axis))
{
  c.m_vao = 0;
  c.m_vbo = 0;
}

Cube& Cube::operator=(Cube &&c) noexcept
{
  deleteGlObjects();

  m_id = c.m_id;
  m_position = std::move(c.m_position);
  m_model = std::move(c.m_model);
  m_vao = c.m_vao;
  m_vbo = c.m_vbo;
  m_angle = c.m_angle;
  m_axis = std::move(c.m_axis);

  c.m_vao = 0;
  c.m_vbo = 0;

  return *this;
}

void Cube::render(const Shader& s)
{
  Rublk& r =  Singleton<Rublk>::instance();
  if (m_angle == 0) {
    r.taskFinished();
    m_angle = -1;
  }
  if (m_angle > 0) {
    m_model = glm::rotate(m_model, glm::radians(3.0f), m_axis);
    m_angle -= 3;
  }
  s.setMat4("model", glm::translate(m_model, m_position));

  glBindVertexArray(m_vao);
  glDrawArrays(GL_TRIANGLES, 0, 36);
  glBindVertexArray(0);
}

glm::vec3 Cube::getPosition() const
{
  return glm::vec3(m_model * glm::vec4(m_position, 1.0f));
}

void Cube::rotate(int degree, const glm::vec3& axis)
{
  m_angle = degree;
  m_axis = glm::vec3(glm::inverse(m_model) * glm::vec4(axis, 1.0f));
  Singleton<Rublk>::instance().taskStart();
}

void Cube::changeModel(int degree, const glm::vec3 &axis)
{
  m_axis = glm::vec3(glm::inverse(m_model) * glm::vec4(axis, 1.0f));
  m_model = glm::rotate(m_model, glm::radians((float)degree), m_axis);
}

